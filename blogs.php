    <script type="text/javascript">
      let currentPage = 1;
const entriesPerPage = 9; // Adjust based on how many entries you want per page
const blogEntries = document.querySelectorAll('.blog-entry');
const totalEntries = blogEntries.length;
const totalPages = Math.ceil(totalEntries / entriesPerPage);

function displayPage(page) {
  // Hide all entries
  blogEntries.forEach(entry => entry.style.display = 'none');
  
  // Calculate start and end indices for the current page
  const start = (page - 1) * entriesPerPage;
  const end = start + entriesPerPage;

  // Show entries for the current page
  for (let i = start; i < end && i < totalEntries; i++) {
    blogEntries[i].style.display = 'block';
  }

  // Update page number display
  document.getElementById('page-number').textContent = page;
}

function nextPage() {
  if (currentPage < totalPages) {
    currentPage++;
    displayPage(currentPage);
  }
}

function prevPage() {
  if (currentPage > 1) {
    currentPage--;
    displayPage(currentPage);
  }
}

// Initialize by showing the first page
displayPage(currentPage);

